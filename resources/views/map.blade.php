    function initMap() {
      var map = new google.maps.Map(document.getElementById('map'), {
        center: new google.maps.LatLng(7.94653, -1.02319),
        zoom: 7
      });
      var infoWindow = new google.maps.InfoWindow;

      var data = <?= $towers ?>;
      console.log(data);
      for(var x in data){
        var tower = data[x];
        var company = tower.comapany;
        var siteId = tower.site_id;
        var district = tower.district;
        var location = tower.location;
        var status = tower.status;
        var point = new google.maps.LatLng(tower.lat, tower.lng);
        var marker = new google.maps.Marker({
          position: point,
          title: tower.company,
          icon: '../assets/img/maps/redtower.png',
          map: map
        });
        var infowincontent = document.createElement('div');
        var strong = document.createElement('strong');
        strong.textContent = company;
        infowincontent.appendChild(strong);
        infowincontent.appendChild(document.createElement('br'));
        
        var text = document.createElement('text');
        text.textContent = "Site ID: " + siteId + "District: " + district
        infowincontent.appendChild(text);
        infowincontent.appendChild(document.createElement('br'));

        var text = document.createElement('text');
        text.textContent = "Location: " + location +  ", Site Status: " + status
        infowincontent.appendChild(text);

        marker.addListener('click', function() {
          infoWindow.setContent(infowincontent);
          infoWindow.open(map, marker);
        });
      }
    }
    
  </script>

